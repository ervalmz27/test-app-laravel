     *                 @OA\Property(property="id", type="integer", example=1),
     *                 @OA\Property(property="name", type="string", example="Laptop Gaming ASUS ROG"),
     *                 @OA\Property(property="description", type="string", example="Laptop gaming dengan spesifikasi tinggi"),
     *                 @OA\Property(property="price", type="string", example="15000000.00"),
     *                 @OA\Property(property="stock", type="integer", example=10),
     *                 @OA\Property(property="created_at", type="string", format="date-time"),
     *                 @OA\Property(property="updated_at", type="string", format="date-time")
     *             )
     *         )
     *     ),
     *     @OA\Response(
     *         response=401,
